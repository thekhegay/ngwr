import type { WrHsv } from './wr-hsv';
import type { WrRgb } from './wr-rgb';

/**
 * Convert sRGB to HSV. Preserves alpha.
 */
export function rgbToHsv(rgb: WrRgb): WrHsv {
  const r = rgb.r / 255;
  const g = rgb.g / 255;
  const b = rgb.b / 255;
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  const d = max - min;

  let h = 0;
  if (d !== 0) {
    if (max === r) h = ((g - b) / d) % 6;
    else if (max === g) h = (b - r) / d + 2;
    else h = (r - g) / d + 4;
    h *= 60;
    if (h < 0) h += 360;
  }

  const s = max === 0 ? 0 : d / max;
  const v = max;

  return { h, s, v, a: rgb.a };
}
