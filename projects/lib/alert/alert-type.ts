/**
 * Supported alert types.
 *
 * - `'info'`    – default / primary state
 * - `'success'` – success state
 * - `'warning'` – warning state
 * - `'danger'`  – error / critical state
 */
export type WrAlertType = 'info' | 'success' | 'warning' | 'danger';
